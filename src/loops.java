public class loops {
    //while loop
    //for loop
    //do while loop


    //for loops formula
    public static void main(String[] args) {
        for (int i = 1; i <= 6; i++) {
            System.out.println(i);
        }



        //while loops
        int i =1;
        while(i<=8){
            System.out.println(i);
            i++;  //make sure put the i++ for while loops otherwise it gonna crush you pc

        }

        //do while loops
        int number =1;
        do {
            System.out.println(number);
            number++;
        }
        while(number <= 7);
    }
}
